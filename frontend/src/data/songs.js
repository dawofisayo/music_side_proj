  // Get song for today (deterministic based on date)
  export function getDailySong() {
    const today = new Date();
    const startDate = new Date('2024-01-01');
    const daysSinceStart = Math.floor((today - startDate) / (1000 * 60 * 60 * 24));
    const songIndex = daysSinceStart % SONG_DATABASE.length;
    return SONG_DATABASE[songIndex];
  }
  
  // Get random songs for search/autocomplete
  export function getRandomSongs(count = 20) {
    const shuffled = [...SONG_DATABASE].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
  }